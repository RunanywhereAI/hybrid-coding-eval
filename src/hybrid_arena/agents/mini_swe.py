"""mini-swe-agent runner — SWE-bench Verified, routed through this repo's proxy.

mini-swe-agent (PyPI ``mini-swe-agent``, github.com/SWE-agent/mini-swe-agent) is
the apples-to-apples agent on the official SWE-bench Verified leaderboard
(bash-only column). It is a ~100 LOC ReAct loop that uses LiteLLM for the LM
call, so pointing it at this repo's proxy on :8787 routes every agent step
through the configured strategy.

What this runner does, per task:
  1. Generate a 12-hex ``bench_run_id`` and write a per-strategy YAML
     config that sets LiteLLM ``api_base`` to ``http://127.0.0.1:8787/v1``
     and ``model_name`` to ``openai/router/<strategy>/run-<id>``.
  2. Subprocess ``mini-extra swebench-single`` with that config + the
     instance ID. Cap at a hard timeout.
  3. Parse the produced trajectory file for the final unified diff.
  4. Score via the existing :mod:`hybrid_arena.scorers.swebench`
     scorer (Docker SWE-bench harness, ~10 min/task under Rosetta).
  5. Reconstruct per-call token attribution from
     ``router/logs/decisions.jsonl`` by filtering on ``bench_run_id``.

The runner is intentionally a thin wrapper: no model invocation here — that
happens inside the mini-swe-agent subprocess.
"""

from __future__ import annotations

import json
import os
import shutil
import subprocess
import sys
import time
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from hybrid_arena.agents.attribution import (
    attribute_from_decisions_log,
    generate_run_id,
    model_string,
)
from hybrid_arena.core.metrics import Latency, Quality, ResultRow
from hybrid_arena.core.paths import repo_root as _resolve_repo_root

__all__ = ["run", "ROUTE"]

ROUTE = "mini-swe-agent"

_REPO_ROOT: Path = _resolve_repo_root()

#: Hard wall-clock cap per task. mini-swe-agent's default cost-limit may
#: also fire earlier. 25 min is the budget the plan reserves per task.
DEFAULT_TIMEOUT_S: int = 1500


def _task_slug(task_id: str) -> str:
    return task_id.replace("/", "__").replace(" ", "_")


#: mini-swe-agent ships a default SWE-bench config (with system/instance
#: templates etc.) inside its package. We layer our model + cost overrides
#: ON TOP of this via two -c flags.
def _default_swebench_yaml() -> Path:
    import minisweagent
    return Path(minisweagent.__file__).parent / "config" / "benchmarks" / "swebench.yaml"


def _strategy_yaml(api_base: str, strategy: str, bench_run_id: str) -> str:
    """Inline YAML the mini-extra CLI consumes via ``-c <file>``.

    Layered ON TOP of the package's ``swebench.yaml`` (which provides
    ``agent.system_template`` + ``agent.instance_template``), so we only
    need to override the model + cost-tracking knobs here.

    The ``/run-<id>`` suffix on ``model_name`` is parsed by
    ``router/server.mjs`` and echoed into every decisions.jsonl row,
    enabling exact-match token attribution back to this runner invocation.

    LiteLLM's ``custom_llm_provider: openai`` tells it to use the OpenAI
    adapter and *not* try to resolve the model id against the model
    registry — this is what makes ``openai/router/...`` work.
    """
    model_name = model_string(strategy, bench_run_id, prefix="openai/router")
    return f"""\
# Auto-generated by hybrid_arena.agents.mini_swe (layered on package swebench.yaml)
model:
  model_name: {model_name}
  model_kwargs:
    api_base: {api_base}
    api_key: bench-eval-key
    custom_llm_provider: openai
    temperature: 0.2
  cost_tracking: ignore_errors
"""


def _extract_diff_from_trajectory(traj_path: Path) -> str:
    """mini-swe-agent v2 writes the final patch to ``info.submission`` in
    the trajectory JSON (or as a `git diff` block in the last message).

    Returns the empty string if no diff was produced.
    """
    if not traj_path.exists():
        return ""
    try:
        traj = json.loads(traj_path.read_text(encoding="utf-8"))
    except Exception:
        return ""
    # v2 schema: top-level "info" dict with "submission" or "extracted_output"
    info = traj.get("info") or {}
    for key in ("submission", "extracted_output", "patch"):
        v = info.get(key)
        if isinstance(v, str) and v.strip():
            return v
    # Fallback: scan messages for a `git diff` block in the last assistant
    msgs = traj.get("messages") or traj.get("history") or []
    for m in reversed(msgs):
        content = m.get("content") if isinstance(m, dict) else None
        if isinstance(content, str) and "diff --git" in content:
            idx = content.find("diff --git")
            return content[idx:]
    return ""


def run(
    task: Any,
    *,
    proxy_url: str = "http://127.0.0.1:8787",
    hardware_profile_ref: str = "",
    output_dir: Path | None = None,
    router_strategy: str = "heuristic",
    timeout_s: int = DEFAULT_TIMEOUT_S,
    **_unused: Any,
) -> ResultRow:
    """Run one SWE-bench Verified instance through mini-swe-agent.

    Returns a :class:`ResultRow` with ``route="mini-swe-agent"`` and
    ``router_strategy`` populated. Token attribution is reconstructed
    from ``router/logs/decisions.jsonl`` (timestamp-window).
    """
    if output_dir is None:
        output_dir = _REPO_ROOT / "results" / "mini-swe-agent"
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    slug = _task_slug(task.id)
    run_dir = output_dir / f"mini-swe-agent_{slug}_{router_strategy}"
    run_dir.mkdir(parents=True, exist_ok=True)

    # 1. Generate correlation id + write per-strategy YAML config.
    bench_run_id = generate_run_id()
    api_base = proxy_url.rstrip("/") + "/v1"
    cfg_path = run_dir / "mswea-config.yaml"
    cfg_path.write_text(
        _strategy_yaml(api_base, router_strategy, bench_run_id), encoding="utf-8"
    )

    traj_path = run_dir / "trajectory.json"

    # 2. Subprocess invocation.
    # Pass TWO -c flags: the package's default swebench.yaml (system +
    # instance templates) first, then our model-override yaml. mini-extra
    # merges configs in order, so ours wins on the keys it sets.
    #
    # Prefer .venv/bin/mini-extra when present (the v1.4 arena setup
    # installs ``mini-swe-agent`` into the repo's venv); fall back to
    # PATH lookup. Without this, ``.venv/bin/python -m
    # hybrid_arena.cli.run`` would fail because activating a venv's
    # python does NOT prepend its bin/ to PATH automatically.
    _venv_mini_extra = _REPO_ROOT / ".venv" / "bin" / "mini-extra"
    mini_extra_bin = (
        str(_venv_mini_extra) if _venv_mini_extra.exists()
        else (shutil.which("mini-extra") or "mini-extra")
    )
    default_swebench_yaml = _default_swebench_yaml()
    cmd = [
        mini_extra_bin,
        "swebench-single",
        "--yolo",
        "--subset",
        "verified",
        "--split",
        "test",
        "-i",
        task.instance_id,
        "-c",
        str(default_swebench_yaml),
        "-c",
        str(cfg_path),
        "-o",
        str(traj_path),
        "-l",
        "0",  # disable cost-limit; rely on wall timeout
    ]

    env = os.environ.copy()
    env["OPENAI_API_KEY"] = env.get("OPENAI_API_KEY", "bench-eval-key")

    started_at = datetime.now(timezone.utc)
    t0 = time.perf_counter()
    err: str | None = None
    returncode = -1
    try:
        proc = subprocess.run(
            cmd,
            timeout=timeout_s,
            capture_output=True,
            text=True,
            env=env,
            check=False,
        )
        # Stash stdout/stderr for debugging.
        (run_dir / "stdout.log").write_text(proc.stdout or "", encoding="utf-8")
        (run_dir / "stderr.log").write_text(proc.stderr or "", encoding="utf-8")
        returncode = proc.returncode
    except subprocess.TimeoutExpired as exc:
        err = f"agent_timeout_{timeout_s}s"
        (run_dir / "stderr.log").write_text(
            f"TimeoutExpired after {timeout_s}s: {exc}\n", encoding="utf-8"
        )
    except FileNotFoundError:
        err = "mini_swe_agent_not_installed"

    wall_ms = int((time.perf_counter() - t0) * 1000)
    finished_at = datetime.now(timezone.utc)

    # 3. Extract diff (may be empty on failure).
    diff_text = _extract_diff_from_trajectory(traj_path)
    answer_path = run_dir / "answer.txt"
    answer_path.write_text(diff_text, encoding="utf-8")

    # Mini-swe-agent v2 sometimes exits non-zero due to an asyncio cleanup
    # issue on macOS even when the agent successfully produced a diff. Only
    # treat it as an error if there is no diff to score.
    if err is None and returncode != 0:
        if not diff_text.strip() and "diff --git" not in diff_text:
            err = f"mini_swe_agent_exit_{returncode}"
        # else: leave err=None; the diff is present, scorer will grade it.

    # 4. Attribute tokens from decisions.jsonl (primary: bench_run_id;
    #    fallback: timestamp window for older logs).
    tokens, routing = attribute_from_decisions_log(
        run_id=bench_run_id,
        strategy=router_strategy,
        started_at=started_at,
        finished_at=finished_at,
    )

    # 5. Build relative output_ref so the JSONL is portable across machines.
    try:
        output_ref = str(answer_path.resolve().relative_to(_REPO_ROOT))
    except ValueError:
        output_ref = str(answer_path.resolve())

    row = ResultRow(
        task_id=task.id,
        category=getattr(task, "category", "real-prs"),
        route=ROUTE,
        hardware_profile_ref=hardware_profile_ref,
        tokens=tokens,
        latency=Latency(wall_ms=wall_ms, per_call_ms=[wall_ms]),
        quality=Quality(),  # scorer fills this in via experiment.score_row
        routing=routing,
        output_ref=output_ref,
        started_at=started_at.isoformat(),
        finished_at=finished_at.isoformat(),
        error=err,
        router_strategy=router_strategy,
    )
    return row


# Command-line shim for manual debugging:
#   python -m hybrid_arena.agents.mini_swe --task-id swebench-verified/django__django-11163 --strategy heuristic
def _main(argv: list[str] | None = None) -> int:
    import argparse

    ap = argparse.ArgumentParser()
    ap.add_argument("--task-id", required=True)
    ap.add_argument("--strategy", default="heuristic")
    ap.add_argument("--out", type=Path, required=True)
    ap.add_argument("--proxy-url", default="http://127.0.0.1:8787")
    ap.add_argument("--timeout-s", type=int, default=DEFAULT_TIMEOUT_S)
    args = ap.parse_args(argv)

    from hybrid_arena.tasks.real_prs.adapter import load_tasks

    all_tasks = load_tasks()
    matches = [t for t in all_tasks if t.id == args.task_id or t.instance_id == args.task_id]
    if not matches:
        print(f"no task with id {args.task_id!r}", file=sys.stderr)
        return 1
    row = run(
        matches[0],
        proxy_url=args.proxy_url,
        output_dir=args.out,
        router_strategy=args.strategy,
        timeout_s=args.timeout_s,
    )
    print(json.dumps(row.to_dict(), indent=2, default=str))
    return 0 if not row.error else 2


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(_main())
