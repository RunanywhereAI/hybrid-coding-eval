#!/usr/bin/env python3
"""Aggregate per-strategy R3 metrics across the v3.2 sweep run-dirs.

Reads results/runs/{12..16}-r3-strategy-*/raw.jsonl and produces:
- Per-strategy summary (cost, cloud_fraction, pass-rate) per category
- A Markdown table ready to drop into reports/ARTICLE.md §3.5

Usage:
    python bin/v3.3-aggregate-strategy.py                    # print to stdout
    python bin/v3.3-aggregate-strategy.py > out.md           # save

Requires raw.jsonl in each variant dir. Variants that haven't finished
yet are skipped with a note.
"""
from __future__ import annotations

import json
import sys
from pathlib import Path
from collections import defaultdict
from statistics import median

REPO = Path(__file__).resolve().parent.parent
RUNS = REPO / "results" / "runs"
STRATEGIES = ["heuristic", "rules", "llm-classifier", "embedding-knn", "cascade"]
PRICE = json.loads((REPO / "configs/pricing/pricing_tables.json").read_text())["rates_per_m"]["gpt-5.5"]


def cost(row):
    """Re-derive cost from tokens under the gpt-5.5 primary scenario."""
    t = row["tokens"]
    cached = t.get("cache_read", 0)
    input_ = t.get("cloud_prompt", 0) - cached
    output = t.get("cloud_completion", 0)
    return (
        input_ * PRICE["input"] / 1_000_000
        + cached * PRICE.get("cache_read", PRICE["input"]) / 1_000_000
        + output * PRICE["output"] / 1_000_000
    )


def cloud_fraction(row):
    t = row["tokens"]
    cloud = t.get("cloud_prompt", 0) + t.get("cloud_completion", 0)
    local = t.get("local_prompt", 0) + t.get("local_completion", 0)
    total = cloud + local
    return cloud / total if total else 0.0


def passing(row):
    """Functional pass for A/B/C-bcb/D1/D5; composite>=0.5 for C-arch/D3/D4."""
    q = row.get("quality") or {}
    if q.get("functional_pass") is not None:
        return 1 if q["functional_pass"] else 0
    if q.get("composite") is not None:
        return 1 if q["composite"] >= 0.5 else 0
    return None


def shape_of(row):
    """A/B/C-bcb/C-arch/D1/D2/D3/D4/D5."""
    src = row.get("source") or ""
    if "humaneval" in src:
        return "A"
    if "swebench" in src:
        return "B"
    if "bigcodebench" in src:
        return "C-bcb"
    if "custom_arch" in src or "custom-arch" in src:
        return "C-arch"
    task = row.get("task_id") or ""
    for s in ("d1", "d2", "d3", "d4", "d5"):
        if f"/{s}-" in task or task.startswith(f"{s}-"):
            return s.upper()
    return "?"


def aggregate(rows):
    """Return dict shape → (n, n_pass, median_cost, median_cloud_frac, median_wall)."""
    by_shape = defaultdict(list)
    for r in rows:
        by_shape[shape_of(r)].append(r)
    out = {}
    for sh, rs in by_shape.items():
        n = len(rs)
        passes = [p for p in (passing(r) for r in rs) if p is not None]
        n_pass = sum(passes) if passes else None
        costs = [cost(r) for r in rs]
        clouds = [cloud_fraction(r) for r in rs]
        walls = [(r.get("latency") or {}).get("wall_ms", 0) for r in rs]
        out[sh] = {
            "n": n,
            "n_pass": n_pass,
            "med_cost": median(costs) if costs else 0,
            "med_cloud_frac": median(clouds) if clouds else 0,
            "med_wall_ms": median(walls) if walls else 0,
        }
    return out


def load_variant(variant_dir):
    p = variant_dir / "raw.jsonl"
    if not p.exists():
        return None
    rows = []
    for line in p.read_text().splitlines():
        if line.strip():
            rows.append(json.loads(line))
    return rows


def main():
    print("# v3.2 strategy sweep — per-strategy R3 aggregate")
    print()
    print("Generated by `bin/v3.3-aggregate-strategy.py`. Updates as variants land.")
    print()
    print("## Per-strategy summary (R3 only, devstral:24b local, gpt-5.5 cloud)")
    print()
    print("| strategy | n_rows | A pass | B pass | C-bcb pass | C-arch pass | D1 pass | D2 pass | D3 pass | D4 pass | D5 pass | sum cost | median wall ms | cloud_frac (median) |")
    print("| --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: |")
    for s in STRATEGIES:
        dir_ = RUNS / f"{12 + STRATEGIES.index(s):02d}-r3-strategy-{s}"
        rows = load_variant(dir_)
        if rows is None:
            print(f"| {s} | _pending_ | | | | | | | | | | | | |")
            continue
        agg = aggregate(rows)
        total_cost = sum(cost(r) for r in rows)
        med_cloud = median([cloud_fraction(r) for r in rows]) if rows else 0
        med_wall = median([(r.get('latency') or {}).get('wall_ms', 0) for r in rows]) if rows else 0

        def cell(sh):
            a = agg.get(sh)
            if not a:
                return "—"
            if a["n_pass"] is None:
                return f"None/{a['n']}"
            return f"{a['n_pass']}/{a['n']}"

        print(f"| {s} | {len(rows)} | {cell('A')} | {cell('B')} | {cell('C-bcb')} | {cell('C-arch')} | {cell('D1')} | {cell('D2')} | {cell('D3')} | {cell('D4')} | {cell('D5')} | ${total_cost:.2f} | {int(med_wall)} | {med_cloud:.0%} |")

    print()
    print("## Notes")
    print()
    print("- `n_rows` should reach 50 per strategy when the v3.2 sweep is complete.")
    print("- `_pending_` rows = variant hasn't finished writing raw.jsonl yet.")
    print("- Cost is derived under the openai-gpt5.5 primary scenario; re-price via `./bench token-budget`.")
    print("- C-arch, D3, D4 pass = composite ≥ 0.5 (judge-scored).")
    print("- D2 pass = None/N by design (functional scorer deferred).")


if __name__ == "__main__":
    main()
